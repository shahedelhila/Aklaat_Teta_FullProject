def seed_recipes():
    """تُشغَّل مرة واحدة عند تهيئة التطبيق — تتجاهل الوصفات الموجودة."""
    if Recipe.query.count() > 0:
        return  # البيانات موجودة مسبقاً

    for data in RECIPES_DATA:
        recipe = Recipe(
            id          = data["id"],
            title       = data["title"],
            category    = data["category"],
            image       = data.get("image", ""),
            story       = data.get("story", ""),
            prep_time   = data.get("prep_time"),
            difficulty  = data.get("difficulty", "متوسطة"),
            is_featured = data.get("is_featured", False),
        )
        db.session.add(recipe)

        for idx, ing_text in enumerate(data.get("ingredients", [])):
            db.session.add(
                Ingredient(recipe_id=recipe.id, text=ing_text, order=idx)
            )

        for idx, step_text in enumerate(data.get("steps", [])):
            db.session.add(
                Step(recipe_id=recipe.id, text=step_text, order=idx)
            )

    db.session.commit()
    print(f"✅ تم بذار {len(RECIPES_DATA)} وصفة في قاعدة البيانات")
